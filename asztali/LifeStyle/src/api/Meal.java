package api;

public class Meal {
	 String mealname;
	 int calorievalue;
	 int fat;
	 int protein;
	 int carbohydrate;
	 int salt;
	public Meal() {}
	public Meal (String mealname,
	 int calorievalue,
	 int fat,
	 int protein,
	 int carbohydrate,
	 int salt) {
		this.mealname=mealname;
		this.calorievalue=calorievalue;
		this.fat=fat;
		this.protein=protein;
		this.carbohydrate=carbohydrate;
		this.salt=salt;
		
	}
	

}
